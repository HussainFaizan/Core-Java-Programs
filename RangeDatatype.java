class RangeDatatype
{
	public static void main(String []args)
	{
		System.out.println("Datatype       No of Byte        MINVALUE              MAXVALUE");
		System.out.println(Byte.TYPE+"\t\t"+Byte.BYTES+"\t\t"+Byte.MIN_VALUE+"\t\t\t"+Byte.MAX_VALUE);
		System.out.println(Short.TYPE+"\t\t"+Short.BYTES+"\t\t"+Short.MIN_VALUE+"\t\t\t"+Short.MAX_VALUE);
		System.out.println(Integer.TYPE+"\t\t"+Integer.BYTES+"\t\t"+Integer.MIN_VALUE+"\t\t"+Integer.MAX_VALUE);
		System.out.println(Long.TYPE+"\t\t"+Long.BYTES+"\t\t"+Long.MIN_VALUE+"\t"+Long.MAX_VALUE);
	}
}